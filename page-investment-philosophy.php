<script>
document.addEventListener('DOMContentLoaded', function() {
     // Add offset 500 only to the first 3 items for desktop view
     const strategyItems = document.querySelectorAll('.Strategy-herobanner .concept-startegybox .col-lg-4');
    const isDesktop = window.innerWidth >= 992; // Check if the screen width is desktop size

    if (isDesktop) {
        strategyItems.forEach((item, index) => {
            if (index < 5) {
                item.setAttribute('data-aos-offset', '100');
            }
        });
    }
    // Initialize AOS first
    AOS.init({
        offset: 100,
        duration: 800,
        once: true,
        easing: 'ease-out-quad'
    });

    // Handle lazyloaded images
    const observer = new MutationObserver((mutations) => {
        mutations.forEach((mutation) => {
            if (mutation.type === 'attributes' && mutation.attributeName === 'class') {
                if (mutation.target.classList.contains('lazyloaded')) {
                    setTimeout(() => {
                        AOS.refresh();
                        // Also reinitialize Waypoint if needed
                        if (typeof Waypoint !== 'undefined') {
                            Waypoint.refreshAll();
                        }
                    }, 100);
                }
            }
        });
    });

    // Watch for image lazyload
    const images = document.querySelectorAll('img.lazyload');
    images.forEach(img => {
        observer.observe(img, {
            attributes: true
        });
    });
});


    </script>